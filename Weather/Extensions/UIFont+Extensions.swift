import UIKit

extension UIFont {
    
    static func robotoRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Regular", size: size)!
    }
    
    static func robotoMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Medium", size: size)!
    }
    
    static func circeRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Circe-Regular", size: size)!
    }
    
    static func circeBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Circe-Bold", size: size)!
    }
    
    static func k2dMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "K2D-Medium", size: size)!
    }
    
    static func k2dBold(size: CGFloat) -> UIFont {
        return UIFont(name: "K2D-Bold", size: size)!
    }
    
    
}
